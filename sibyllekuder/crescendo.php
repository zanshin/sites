		<h1>Crescendo Now, Pay Later</h1>
		<p>
			In the hierarchy of musical priorities, dynamics have often appeared to me to be
			relegated to fairly low niche and left there to languish, their immense potential
			for beauty and expressiveness being overlooked and ignored. Even in recordings of
			professional orchestras it is not at all unusual to here a crescendo or diminuendo
			begin and end without any unified idea of where it is heading. Concerts or recordings
			where the music making is otherwise of a very high calibe all to often approach
			the dynamics with far less care and intelligence than other musical matters.
		</p>
		<p>
			Every musician knows what dynamics are and will tell you that when they see an mf on
			the music, by golly they play mezzoforte! And when they see a crescendo to a fff,
			watch out, buddy!
		</p>
		<p>
			Crescendos (or crescendi, for our purist friends) seem to bring out a primal Darwinian,
			survival of the fittest impulse in many of us. If you can do it, e.g. low brass, most
			percussion, etc., then do it. If you can't, e.g. low register flutes, middle register
			clarinets, etc., then get out of the way. It is quite understandable. After all, you
			flutes and clarinets get all the great melodies and technical calisthenics while we
			timpanists and trombonists are stuck back here counting rests. So, when we are finally
			allowed to speak, our voices will be heard! Then to our delight and to the delight of
			our audiences, excitement happens! Everybody wins, right? Absolutely. The only casualty
			is the music.
		</p>
		<p>
			I don't wish to claim any tremendous pre-eminence in this area, but I have listened to
			what others have to say on the topic and have explored a number of approaches on my own.
			I also don't wish to suggest that any of my findings are original with me. Au contraire,
			anyone with keen musical sensitivities would be very apt to make similar explorations
			and discoveries on their own. So with your kind indulgence, perhaps we can investigate
			a few of these principals together. If you find something here that might squeeze your
			own creative juices, then my fondest hopes will have been realized. If not, then I
			recommend you demand a full refund from the author.
		</p>
		<p>
			Why don't we continue flogging the subject of the crescendo for just a moment or two?
			Most of them seem to have a destination or a specific point in the music when the energy
			and momentum gathered by it is released in a heightened moment of drama or intensity.
			Obviously, for the full dramatic potential of the passage to be realized there has to
			be general agreement exactly where and when that moment will arrive. If a few people in
			the ensemble peak on the crescendo prematurely the effect is spoiled, and the audience
			slinks out of the hall feeling violated and unclean.
		</p>
		<p>
			A question that is too rarely asked is, "What should the shape of the crescendo be?" The
			written symbol has two converging straight lines, yet in most instances, to shape the
			crescendo like a trumpet bell would be musically and dramatically much more effective,
			i.e. very little crescendo at first, then gradually increasing the volume, saving the
			final 50% of the crescendo for the last 10% of the passage. You may wish to experiment
			with this idea in a few select passages to see if you agree with the results. My guess
			is that you will be heralded as a genius and promoted at least as high the custodial
			staff.
		</p>
		<p>
			Another oft-neglected question is, "Who should crescendo when?" May I offer a suggestion
			here, also?
		</p>
		<p>
			A typical passage containing a crescendo might consist of the melodic material in the
			upper voices, the harmonic or accompanimental figures in the middle voices, a bass
			line, and some rhythmic figures in the percussion. If you were to encourage the melodic
			instruments to begin the crescendo first and the accompaniment to construct their
			crescendo entirely in support of the melody, but never upstaging it, you will find
			that the melody will maintain its primacy. Now, if through coercion or perhaps even
			bribery, you can appeal to the percussionists' better angels and make them aware of
			the ever-present danger of premature gesticulation, you may have crated the ideal
			crescendo. If the percussionists save a solid 80% of their crescendo until the last
			couple of beats before its apex, they will provide the perfect dot for the perfect
			"i" and prove once again, even to the most skeptical, that, except in certain one-on-one
			disciplinary actions, intelligence and sensitivity trump brute force every time.
		</p>
		<p>
			It is my fervent hope to continue to lobby for the liberation of the crescendo. If
			there is any true justice I have recruited you to become a like-minded zealot to this
			most noble of causes.
		</p>
		<p>
			Any comments about your own glorious victories you might wish to contribute would, no
			doubt, serve as an enduring inspiration to us all.
		</p>
