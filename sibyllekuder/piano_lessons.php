		<h1>Piano Lessons for Young Children</h1>
		<h2>Getting the Most Out of Piano Lessons</h2>
		<p>Posted by Giles Communications, LLC</p>
		<p>
			It's an image engraved on our cultural memory, right next to skate keys and doses of
			castor oil: the child before the piano, forced to practice scales while friends play
			ball outside. Today, with the advent of new approaches, new teaching methods and new
			attitudes, that's all changed. According to the National Piano Foundation, a few
			simple guidelines will heighten the fun of piano instruction and help you make sure
			your child gets the most out of it.
		</p>
		<p>
			In fact, learning to play the piano is one of the most rewarding things a child can
			accomplish. A child can have as much fun during the learning process as an accomplished
			pianist has in performance.
		</p>
		<p>
			Parents should begin by deciding when to start a child's piano instruction. Seven or
			eight is an average age, but kids can start earlier or later--and, as millions of
			adult piano students can attest, it's never too late to begin. Pay attention to your
			child's interest in music, attention span and eagerness to learn for indications of
			when the time is right.
		</p>
		<p>
			Choosing a teacher is an important step, so don't let cost and convenience be the
			sole factors. Interview candidates, and ask to listen in on their lessons. Ask to see
			the lesson plan for the first few months; even if you're not a musician, you'll be able
			to tell if it contains clear progress goals. Some kids may prefer individual
			instruction, while others may get more out of group lessons, so investigate both
			options. Find out what professional organizations the teacher is affiliated with.
			Finally, evaluate the teacher's personality, and how well you think he or she will
			get along with your child. When it's lesson time, the rapport between student and
			teacher can make the time fly--or crawl.
		</p>
		<p>
			Whether or not you play the piano, there's a lot you can contribute to your child's
			enjoyment and success. Show enthusiasm for the process, and sit in on a lesson if the
			teacher feels it's appropriate. Be available, whether it's for giving pointers, listening
			appreciatively to what your child has learned, or even just for driving to lessons.
			Finally, be the grown-up and provide structure. Young children shouldn't decide whether
			or when to practice the piano, any more than they should decide their own bedtimes.
			And never let children practice when they're tired; doing it well requires the ability
			to concentrate. If you make it clear what you expect from your child, then contribute
			your own cheerful, patient involvement, the results will amaze you.
		</p>
		<p>
			Provide a good environment for your child's lessons. Make sure not only to have a
			well-tuned piano with a properly sized bench, but also a quiet room, good lighting,
			and freedom from distractions such as television, radio and other people's activities.
			Make sure other members of the family know and respect how important your child's
			piano time is.
		</p>
		<p>
			If there's enough time, you should also try to schedule lessons and practice sessions
			so that your child doesn't miss out on other activities they enjoy. Kids need to play
			ball, too, and they'll have more fun at the piano if they get to do both.
		</p>
		<p>
			Every child is an individual, so create expectations that conform to your child's
			abilities and interests. See to it that he or she has a chance to play fun music,
			pieces that hold interest, in addition to the more difficult assignments that are
			necessary for progress. Children should be encouraged to make music their own: they
			shouldn't feel as if they have to play as well as their favorite stars on the radio.
			By playing at their own pace, going slowly at first over difficult parts to build up
			muscle memory, they'll be on the road to true mastery.
		</p>
		<p>
			Finally, don't impose unrealistic long-term goals. Not every kid in the schoolyard is
			going to be a professional athlete, and not every kid taking piano lessons is going
			to play Carnegie Hall. Some kids will indeed become virtuosos, but playing for fun
			comes first. It's okay for the pure enjoyment of the piano to be your only reason for
			learning. And remember, music lasts a lifetime.
		</p>

