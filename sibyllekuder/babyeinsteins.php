		<h1>Baby Einsteins: Not So Smart After All</h1>
		<h2>by Alice Park</h2>
		<p>
			The claim always seemed too good to be true: park your infant in front of a video and,
			in no time, he or she will be talking and getting smarter than the neighbor's kid. In
			the latest study on the effects of popular videos such as the "Baby Einstein" and
			"Brainy Baby" series, researchers find that these products may be doing more harm than
			good. And they may actually delay language development in toddlers.
		</p>
		<p>
			Led by Frederick Zimmerman and Dr. Dimitri Christakis, both at the University of
			Washington, the research team found that with every hour per day spent watching baby
			DVDs and videos, infants learned six to eight fewer new vocabulary words than babies
			who never watched the videos. These products had the strongest detrimental effect on
			babies 8 to 16 months old, the age at which language skills are starting to form.
			"The more videos they watched, the fewer words they knew," says Christakis. "These
			babies scored about 10% lower on language skills than infants who had not watched
			these videos."
		</p>
		<p>
			It's not the first blow to baby videos, and likely won't be the last. Mounting evidence
			suggests that passive screen sucking not only doesn't help children learn, but could
			also set back their development. Last spring, Christakis and his colleagues found that
			by three months, 40% of babies are regular viewers of DVDs, videos or television; by
			the time they are two years old, almost 90% are spending two to three hours each day in
			front of a screen. Three studies have shown that watching television, even if it
			includes educational programming such as Sesame Street, delays language development.
			"Babies require face-to-face interaction to learn," says Dr. Vic Strasburger, professor
			of pediatrics at the University of New Mexico School of Medicine and a spokesperson for
			the American Academy of Pediatrics. "They don't get that interaction from watching TV
			or videos. In fact, the watching probably interferes with the crucial wiring being laid
			down in their brains during early development." Previous studies have shown, for
			example, that babies learn faster and better from a native speaker of a language when
			they are interacting with that speaker instead of watching the same speaker talk on a
			video screen. "Even watching a live person speak to you via television is not the same
			thing as having that person in front of you," says Christakis.
		</p>
		<p>
			This growing evidence led the Academy to issue its recommendation in 1999 that no child
			under two years old watch any television. The authors of the new study might suggest
			reading instead: children who got daily reading or storytelling time with their parents
			showed a slight increase in language skills.
		</p>
		<p>
			Though the popular baby videos and DVDs in the Washington study were designed to
			stimulate infants' brains, not necessarily to promote language development, parents
			generally assume that the products' promises to make their babies smarter include
			improvement of speaking skills. But, says Christakis, "the majority of the videos don't
			try to promote language; they have rapid scene changes and quick edits, and no
			appearance of the 'parent-ese' type of speaking that parents use when talking to their
			babies."
		</p>
		<p>
			As far as Christakis and his colleagues can determine, the only thing that baby videos
			are doing is producing a generation of overstimulated kids. "There is an assumption that
			stimulation is good, so more is better," he says. "But that's not true; there is such a
			thing as overstimulation." His group has found that the more television children watch,
			the shorter their attention spans later in life. "Their minds come to expect a high
			level of stimulation, and view that as normal," says Christakis, "and by comparison,
			reality is boring."
		</p>
		<p>
			He and other experts worry that the proliferation of these products will continue to
			displace the one thing that babies need in the first months of life - face time with
			human beings. "Every interaction with your child is meaningful," says Christakis.
			"Time is precious in those early years, and the newborn is watching you, and learning
			from everything you do." So just talk to them; they're listening.
		</p>
