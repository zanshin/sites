		<h1>Mastery</h1>
		<p>
			I enjoy teaching all ages and levels, and my goal is always to teach towards mastery.
			Mastery is different from perfection. A piece may be "perfect" but the skills necessary
			to perform the piece may not have been mastered.
		</p>
		<h2>About teaching beginners:</h2>
		<p>
			I particularly enjoy laying a solid foundation for future study. People tend to think
			that the first year or so of piano study is not as important because the student
			is "only" a beginner, but they couldn't be more wrong! Having to re-teach and re-learn
			after the student was allowed to acquire bad habits is frustrating to both teacher
			and student.
		</p>
		<p>
			The following is from an article by Bruce Berr, first published in the Autumn 1999
			issue of "Keyboard Companion."
		</p>
		<blockquote>
		<p>
			Newer teachers sometimes assume that because students are at an elementary level, they
			cannot play their pieces with mastery and artistry - this is not true! This is a matter
			of confusing standard with level. Instruction on any musical instrument is based on
			mastery learning. This hinges on the highly-successful completion of each unit of study
			along the way, especially and particularly the first few. Since students have varying
			levels of aptitude, and learn at different paces and in different ways, the main variant
			should be how much time and reinforcement is needed for that mastery, not the degree to
			which that mastery occurs.
		</p>
		<p>
			To be more specific, when a well-taught student at any level successfully learns a
			piece, the student's performance is virtually as good as the teacher's:
		</p>
		<ul>
			<li>The physical approach is reliable and natural.
			<li>Fingering is consistent and secure.
			<li>Tone quality and rhythm are solid.
			<li>Legato and staccato are clearly played and differentiated.
			<li>Dynamics and dynamic differences are boldly projected.
			<li>The performance authentically communicates the title and mood to a large degree, to any music listener (not just the trained ear of the teacher).
			<li>There is flexibility in all of the above (except fingering!); one slight change in something, intended or unintended, does not cause a cascading failure and meltdown.
			<li>Playing the piece is enjoyable.
		</ul>
		<p>
			This is true even for the beginner's first few lessons! Yes, perhaps there are subtle
			nuances of shaping and timing and other aspects that a more advanced player might bring
			to an early-level piece. And an older player may understand the music on a deeper
			intellectual and emotional level, but these are not absolutely essential for each
			piece to shine and express. If we focus too much on these exceptions, they can become
			a smoke screen that hides from us an essential fact: if students' final playing of most
			of their pieces is not excellent or very close to it, we are in effect building a
			structure whose foundation is of questionable strength to support what will be added
			on later.
		</p>

		<p>
			Setting a goal of complete mastery right from the start, communicating that goal
			repeatedly to the student, and giving the student the means for meeting that goal - all
			of this acts as a springboard for many good habits: efficient practice, careful
			listening, etc. Conversely, if our initial goals for each piece are not set to the
			highest standards, we sell students short before giving them a chance to fully blossom
			into what they can become.
		</p>
		</blockquote>
