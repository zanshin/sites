		<h2>Just Another Thought</h2>
		<p>
			During the eighteen months that I was a traveling piano teacher, I discovered an unexpected 
			advantage: I got to see the instrument on which my students practiced as well as the setup 
			and environment in which they practiced. Occasionally, a parent would apologetically 
			say, "I am sorry, we only have a - " and then proceed to tell me what they "only" had: a 
			digital keyboard, an old piano that needs tuning, etc. - followed by, "I don't know if that is 
			good enough for lessons."
		</p>
		<p>
			That's a curious thought. It begs the question: how can something that's "not good enough" for 
			teaching be possibly good enough for practicing?
		</p>
		<p>
			Think about it: when students come to the teacher's studio for their lesson, once a week, they 
			get to play and study and try new things and correct old things on the instrument their teacher 
			has. Then they go home and practice, every day, on their instrument. That's a ratio of about 1 
			to 6. If you are afraid your instrument at home is not good enough for lessons - that makes it 
			one "good" instrument to about six inferior instruments. How can you possibly expect to make 
			progress like that?
		</p>
