		<h1>Piano Lessons for Young Children</h1>
		<h2>For Parents of Young Children</h2>
		<p>Concepts involved in LEARNING TO PLAY THE PIANO (pre-reading):
		<ul>
		<li><strong>Piano: notes, keys</strong> [the material we use (absolute)].</li>
		<li class="indent">compare to writing: letters.</li>
		<li class="indent">compare to foreign language: vocabulary.</li>
		<br />
		<li><strong>Piano: patterns</strong></li>
		<li class="indent">compare to writing: words, whole-language concept.</li>
		<li class="indent">compare to foreign language: vocabulary.</li>
		<br />
		<li><strong>Piano: technique</strong> [physical (somewhat individual)].</li>
		<li class="indent">compare to writing: handwriting, how to hold pen, physically forming letters.</li>
		<li class="indent">compare to foreign language: pronunciation.</li>
		<br />
		<li><strong>Piano: theory, rules</strong> (incl. fingering, etc.) [intellectual (absolute but subject to change)].</li>
		<li class="indent">compare to writing: grammar (and spelling, punctuation).</li>
		<li class="indent">compare to foreign language: grammar (and spelling, punctuation).</li>
		<br />
		<li><strong>Piano: create songs, pieces, and musical stories, recreate what you've heard before</strong> (at first, disregard rules and technique).</li>
		<li class="indent">compare to writing: "creative writing" and writing down a story you have heard before.</li>
		<li class="indent">compare to foreign language: speaking, communicating.</li>
		<br />
		</ul>
		<p>
			<strong>Ultimate Goal:</strong>
		</p>
		<ul>
		<li>(re)create songs, pieces and musical stories using correct and appropriate rules and technique, notes and patterns.</li>
		</ul>
.		<p>
			<strong>My goal for the children</strong> is to learn to hear, think and play by
			interval rather than by note/key name.
		</p>
		<p>
			Playing by absolute note/key names promotes vertical thinking which impedes the flow of
			music whereas interval relationships and direction (up, down or repeat) show where
			the music is going.
		</p>
		<p>
			Thinking by key names is similar to spelling e-v-e-r-y w-o-r-d instead of speaking
			fluently.
		</p>
		<p>
			Once we start finger numbers, we will add numbers to solfege singing because they, too,
			indicate interval relationships. This will also help with transposing (playing in
			different keys). Once the children know their note/key names very well, they will
			automatically know that, for instance, "C D E" means do-re-mi, or 1-2-3.
		</p>
		<p>
			Keep in mind that we can handle only one thing at a time: when we try to figure out a
			new part of a song, we have no mental energy left to worry about handshape, - not even
			breathing, it seems! That's ok. You wouldn't correct a toddler's grammar when he
			excitedly tells you what just happened, either. We need to master one thing before
			adding another challenge.
		</p>
		<p>
			Remember that the children are learning to speak a foreign language; eventually they
			will speak and read and write correctly. In your music sharing time at home, it is up
			to you to find the delicate balance between letting your child speak (disregarding
			rules if necessary), and reviewing rules, theory, and technique. Enjoy!
		</p>
		<p>
			No matter how well, or not at all, you as a parent play the piano, I want you to play
			for your child. Make up songs, tunes, sounds, talk about them, perform for each other
			(modeling good listening behavior: listen quietly, don't interrupt). Play games with
			your child; they especially enjoy correcting you when you intentionally mess up:
			play a wrong note in a song we learned, or play with a bad handshape, or play
			unmusically, pounding the piano - let it be very obvious in the beginning, and then, as
			they get better, more subtle.
		</p>
		<p>
			Enjoy the journey, make it an exciting adventure where new discoveries are to be made
			around every corner!
		</p>

