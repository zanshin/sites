		<h1>The Art of the Decrescendo</h1>
		<p>
			The more fortunate among us have experienced that precious, magical moment at the end
			of a performance between the exact instant that the last soft, subtle note from the
			band or orchestra fades from the stage and merges into the forefront of our aesthetic
			sensibilities. Then there is a second or two of hushed, pregnant energy just before
			the spontaneous outburst of fervent adulation from the audience. The more fortunate
			among us have experienced that precious, magical moment at the end of a performance
			between the exact instant that the last soft, subtle note from the band or orchestra
			fades from the stage and merges into the forefront of our aesthetic sensibilities.
			Then there is a second or two of hushed, pregnant energy just before the spontaneous
			outburst of fervent adulation from the audience.
		</p>
		<p>
			Why do these experiences find such resonance within the likes of us? I submit that
			there is a direct relationship between their inner impact and how effectively the
			diminuendo is played. A beautifully tailored diminuendo can magnetically titillate
			the mind of the alert listener and render him reverently helpless in its thrall.
		</p>
		<p>
			Most compositions are richly endowed with these diminuendi or decrescendi, and almost
			every one of them, when approached intelligently and musically, have the potential of
			providing a mini-epiphany for the listener, as well as for the performers.
		</p>
		<p>
			When studying a new score there are certain questions I enjoy asking myself about how
			a particular diminuendo should be approached. These musings are hereby submitted for
			your approval.
		</p>
		<ol>
			<li>Where does the diminuendo begin?</li>
			<li>Can I, or can I not heighten the dramatic effect by maintaining the stronger dynamic level briefly and delaying the diminuendo?</li>
			<li>Does every instrument decrescendo at the same rate, or is the music scored in such a way that in order to retain the primacy of the melody some instruments have to diminuendo less or later than others?</li>
			<li>Will this passage benefit from very little diminuendo at first but increasing exponentially as it nears the end?</li>
			<li>Where does the diminuendo actually end and can I musically afford the risk of allowing it to end prematurely?</li>
			<li>Would it be musically appropriate to slacken the tempo slightly at the end of the decrescendo and quietly revel and luxuriate in it for that extra millisecond?</li>
			<li>What should I be hearing at the end of the diminuendo, and how can I taper it so that the transition into the new dynamic level sounds inevitable rather than abrupt?</li>
		</ol>
		<p>
			In my own private perfect little world the musicians in my band or orchestra, once they
			have tasted a beautifully wrought diminuendo, will in the future, invariably, accept no
			substitutes. They will, in true Pavlovian fashion, salivate uncontrollably at the mere
			prospect of playing the next one.
		</p>
		<p>
			An idyllic but idle fantasy, to be sure. However, you have undoubtedly discovered for
			yourself that any concept you would care to acclimate your ensemble to, such as how to
			approach a decrescendo, can, with sufficient reinforcement, become part of their musical
			intuition.
		</p>
