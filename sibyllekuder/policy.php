		<h2>Studio Policy</h2>
		<p>
			The piano studio is an extension of a private residence. Please remember to respect it as such.
		</p>
		<p>Arriving for your lesson</p>
		<p class="indent">
			Please park in front of the third garage bay (the single door) or along the street. Please do not
			block the first bay, even temporarily.
		</p>
		<p  class="indent">
			Please be aware that I may not be available to open the door if you arrive more than 5 minutes 
			before your lesson time.
		</p>
		<p>Footwear</p>
		<p class="indent">
			Please remove your shoes as you enter the house. You may bring a clean pair of socks to wear in 
			the studio.
		</p>
		<p>Food and Drink</p>
		<p class="indent">
			Please do not bring food into the studio. You are welcome to bring your own bottled water.
		</p>
		<p>Personal Hygiene</p>
		<p class="indent">
			All students must wash their hands immediately before their lesson. Instruments are very expensive and 
			require exceptional respect. Additionally, simple cleanliness and personal hygiene on everyone’s 
			part can help reduce disease transmission.
		</p>
		<p class="indent">
			All guests are asked to sit down to use the toilet - it’s the house rule here.  If boys are not 
			willing to sit down to pee, I ask that you use your bathroom at home before you come for your lesson. 
		</p>
		<p>Lesson format</p>
		<p class="indent">
			Young students, beginners, and most transfer students<sup>*</sup> receive two 30-minutes 
			lessons per week, preferably Monday/Thursday or Tuesday/Friday. As with martial arts, gymnastics 
			and other extra-curricular activities, this more frequent student-teacher contact and 
			interaction reinforces the fundamental skills and practice techniques necessary for a 
			successful, and enjoyable, piano experience. Twice weekly lessons also reduce the potential 
			for incorrect practicing in between lessons, thus allowing us to focus more time on learning and 
			less time on correcting inadvertent mistakes or having to re-learn last week’s concept. I am 
			always more than willing to re-teach something if the student doesn’t understand the concept, but 
			it is a waste of time to have to re-teach simply because too much time has elapsed since the 
			last time we explored the new concept.
		</p>
		<p class="indent">
			The goal is not to progress faster - though it is a welcome side effect - but to provide more 
			frequent feedback, the way it happens in school: no one would think of sending a child to 
			school once a week for 30 minutes, cram a lot of new information in, and then leave the child 
			alone for an entire week. It is usually not until 6th grade that school children ever have to 
			deal with a project the deadline of which is more than a few days away.
		</p>
		<div class="indent">
		<p class="indent">
			<sup>*</sup>Until they are in 4th grade, I consider my students "young". A "beginner" is someone 
			who has not yet finished the equivalent of Level 2B (the orange book) of the Piano Adventure 
			series by Faber&amp;Faber. "Finish the book" means having mastered all of the concepts and being 
			able to play most of the pieces in the book along with the CD. Because of the different approach 
			each teacher has, most transfer students benefit from the more frequent student-teacher 
			interaction as well.
		</p>
		</div>
		<p class="indent">
			Once students are reliably independent in their practicing and play longer pieces 
			which necessitate longer lessons, we switch to once-a-week 45 minute lessons.
		</p>
		<p class="indent">
			I do not offer once-a-week 30-minute lessons.
		</p>
		<p class="indent">
			Individual lessons are available upon request and will be billed at $40 per hour.
		</p>
		<p class="indent">
			Time and scheduling permitting, there will be group and performance classes offered at no 
			additional cost. I will try to schedule group events at everyone's convenience.
		</p>
		<p>
			I offer year-round piano instruction for dedicated students.
		</p>
		<p class="indent">
			The fall and spring semesters coincide with the school calendar, the summer session begins in June.
		</p>
		<p class="indent">
			Focus during the fall and spring semesters is on preparing students for the
			many opportunities for performance and auditions offered by both the Kansas City Music
			Teachers Association and the Manhattan Area Music Teachers Association.
		</p>
		<p class="indent">
			In the past, summer sessions have focused on topics such as The Magic Flute, Peter and
			the Wolf, Carnival of the Animals, Tchaikovsky Discovers America for younger students;
			Film Music, The Piano Music of Frederic Chopin, The Piano Music of Johann Sebastian Bach,
			Counterpoint, Wolfgang Amadeus Mozart, Piano Concertos, Phantom of the Opera and
			others for older, more advanced students.
		</p>
		<p>
			Students of all ages and levels are welcome.
		</p>
		<p class="indent">
			I enjoy teaching all ages and levels, but for elementary school age children, I do require 
			substantial parental involvement until students are at least 10 years old; even parents of older 
			students are always welcome to sit in on lessons.
		</p>
		<p class="indent">
			Particular attention is given to a natural and injury-free technique and to thorough
			mastery of all studied materials.
		</p>
		<p>
			There are limited opportunities to make up or reschedule missed lessons.
		</p>
		<p class="indent">
			As a mother, I understand that children don't normally give you 24 hours notice that
			they will be sick the next day and therefore unable to attend their piano lesson.
			However, the sooner I know that you will have to miss a lesson, the better; it allows
			me to make use of the time, perhaps offering the time to a different student who has
			a scheduling conflict. No-shows will not be made up.
		</p>
		<p class="indent">
			Make-up lessons will be scheduled at my convenience.
		</p>
		<p>
			Any outside performance by the student, be it for school, church or any other venue,
			must be discussed with the teacher.
		</p>
		<p class="indent">
			I take my work as your piano teacher very seriously, and part of my job is to teach how
			to perform. Most students, and parents, underestimate what it takes to perform
			successfully in public. I have very high standards, for myself, and for my students, and
			dismissive comments about a proposed performance, such as, "oh, it doesn't have to be
			perfect, it's just for church ..." are unacceptable.
		</p>
		<p class="indent">
			From the very beginning, I teach mastery, rather than perfection. Bruce Berr, in the
			Autumn 1999 issue of "Keyboard Companion" says, "Newer teachers sometimes assume that
			because students are at an elementary level, they cannot play their pieces with mastery
			and artistry - this is not true! This is a matter of confusing standard with level."
		</p>
		<p>
			Making photocopies in order to avoid purchase is illegal.
		</p>
		<p class="indent">
			Students are expected to purchase all needed music scores. Once a student owns the
			score, we may make photocopies in order to facilitate a page turn, or in order to be
			able to look at all pages of a longer piece at once (particularly useful for visual
			learners). Occasionally, in order to check our level of understanding of a piece, we
			may cut apart the photocopy into individual lines of music and attempt to reassemble
			it, like a puzzle.
		</p>
		<p>
			Practicing.
		</p>
		<p class="indent">
			Practicing is a skill that needs to be taught and learned and practiced.  Practicing 
			is so much more than just playing through a piece numerous times, hoping it will 
			somehow, magically, improve.  One of my goals as a piano teacher is to teach my students 
			how to practice, efficiently and effectively. 
		</p>
		<p class="indent">
			Most children are as enthusiastic about practicing as they are about brushing their
			teeth. And just like they need to be taught, initially, how to brush their teeth, and
			then supervised for quite a while until you are confident they know how and will do it
			correctly, all students need to be taught how to practice (my job), and then supervised
			at home (your job) for a long time.
		</p>
		<p class="indent">
			In my studio, there will be no such thing as a parent sending a child to "go practice!"
			Parental supervision is necessary and should happen in a supportive and loving manner.
			For younger students, this means that you will be in the same room as your child, go
			over the assignment with your child every time he or she sits down to practice, and
			then be there and available to ask leading questions, answer his or her questions,
			comment in a supportive manner on progress made - or lack of progress. Asking older 
			children, "Did you practice today?" may not be good enough. Ask your child to show you 
			what was practiced today. If you do that every day, you will have a very good understanding 
			of his or her progress, or lack thereof. And your child will know to expect that he/she has 
			got to have something to show you.
		</p>

